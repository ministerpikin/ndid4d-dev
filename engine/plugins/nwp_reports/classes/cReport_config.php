		protected function _report_auto_generation(){

			function c_seconds( $a='' ){
				switch ( $a ) {
					case 'ss':
						return 1;
					case 'min':
						return 60;
					case 'hour':
						return 3600;
					case 'days':
						return 24 * 3600;
					case 'week':
						return 7 * 24 * 3600;
					case 'year':
						return 52 * 7 * 24 * 3600;
					default:
						return 0;
				}
			}
			
			$u = new cUsers();
			
			$u->class_settings = $this->class_settings;

			$a1 = new cAccess_roles();
			
			$a1->class_settings = $this->class_settings;

			$this->class_settings['overide_select'] = $this->_get_select() . ", `". $a1->table_name ."`.`". $a1->table_fields['data'] ."` AS 'role', CONCAT(`". $u->table_name ."`.`". $u->table_fields['firstname'] ."`, ' ', `". $u->table_name ."`.`". $u->table_fields['lastname'] ."`) AS 'name', `". $u->table_name ."`.`". $u->table_fields['email'] ."` AS 'email' ";

			$this->class_settings['join'] = "JOIN `". $this->class_settings['database_name'] ."`.`". $u->table_name ."` ON `". $this->table_name ."`.`". $this->table_fields['user'] ."` = `". $u->table_name ."`.`id` ";

			$this->class_settings['join'] .= " JOIN `". $this->class_settings['database_name'] ."`.`". $a1->table_name ."` ON `". $u->table_name ."`.`". $u->table_fields['role'] ."` = `". $a1->table_name ."`.`id`";

			$this->class_settings['where'] = " AND `". $a1->table_name ."`.`". $a1->table_fields['data'] ."` REGEXP '". $this->table_name .".l_notify.l_notify' ";
			
			$us = $this->_get_records();

			if( $us ){

				$d = date("U");
				
				$rid = [];

				$pr = get_project_data();

				$subject = $pr['project_title'] . " AUTO GENERATED REPORT";

				$vat = get_vat_mapping();
				
				foreach ( $us as $sval ) {

					$interval = $sval['interval_value'] * c_seconds( $sval['interval'] );
					
					if( ( $d - (int)$sval['last_notification'] ) > (int)$interval ){
						
						$rid[] = $sval['id'];
						
						$sval['role'] = isset( $sval['role'] ) && $sval['role'] ? json_decode( $sval['role'], true ) : [];
						
						$vat2 = get_report_source_types( $sval['role'] );

						$content = "<p>Kindly be notified of your auto generated reports.</p>";
						
						$content .= "<ol>";
						
						$bff = array_reduce( explode(':::', $sval['report']), function($a, $b) use( $vat, $vat2, $sval ){
							
							$c = '';
							
							if( isset($vat2[ $b ]) && $vat2[ $b ] ){
								
								$url = defined("HYELLA_FRONTEND_URL") && HYELLA_FRONTEND_URL ? HYELLA_FRONTEND_URL : '';

								$url .= '?a=1';
								
								$url .= '&rr='. rawurlencode( base64_encode( $sval['id'] ) );
								
								if( isset($vat[ $b ]) ){
									$url .= '&br='. rawurlencode( base64_encode( $vat[ $b ]) );
								}
								
								$c = '<li><a href="'. $url .'" target="_blank">' .( $vat2[ $b ] ) . " Report". '</a></li>'; 
							}

							return $a . $c;
						});

						$content .= ( $bff ? $bff : '<p class="text-warning"><b>Access Denied</b><br>You do not have access to any report</p>' );
						
						$content .= "</ol>";
						
						$not = array(
							"content" => $content,
							"subject" => $subject,
							"single_email" => $sval[ "email" ],
							"single_name" => $sval[ "name" ]
						);
						
						$this->_send_email_notification( $not, [] );

						usleep(2500);
					}
				}

				if( $rid ){
					$this->_update_records( array(
						'where' => " `id` IN ('". implode("', '", $rid) ."') ",
						'field_and_values' => array(
							$this->table_fields['last_notification'] => [ 'value' => date("U") ]
						)
					) );

					return ['executed' => 1];
				}
			}
			
			return ['executed' => 0];
		}
		
