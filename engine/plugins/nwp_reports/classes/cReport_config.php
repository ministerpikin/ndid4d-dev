		protected function _search_form2(){
			$where = "";
			
			foreach( array( 'customer', 'staff_responsible' ) as $key ){
				if( isset( $this->table_fields[ $key ] ) && isset( $_POST[ $this->table_fields[ $key ] ] ) && $_POST[ $this->table_fields[ $key ] ] ){
					//$this->class_settings[ $key ] = $_POST[ $this->table_fields[ $key ] ];
					$where .= " AND `".$this->table_fields[ $key ]."` = '".$_POST[ $this->table_fields[ $key ] ]."' ";
				}
			}
			
			$key = 'date';
			if( isset( $_POST[ $this->table_fields[ $key ] ] ) && $_POST[ $this->table_fields[ $key ] ] ){
				$this->class_settings[ $key ] = convert_date_to_timestamp( $_POST[ $this->table_fields[ $key ] ] , 1 );
				$where .= " AND `".$this->table_fields[ $key ]."` >= " . $this->class_settings[ $key ];
			}
			
			$key = 'date';
			$skey = "_range";

			if( isset( $_POST[ $this->table_fields[ $key ] . $skey ] ) && $_POST[ $this->table_fields[ $key ] . $skey ] ){
				$this->class_settings[ $key ] = convert_date_to_timestamp( $_POST[ $this->table_fields[ $key ] . $skey ] , 2 );

				$where .= " AND `".$this->table_fields[ $key ]."` <= " . $this->class_settings[ $key ];
			}
			
			$this->class_settings[ "where" ] = $where;
			return $this->_search_form();
		}

		protected function _autogenerated_report_handler( $o = array() ){
			$error_msg = '';
			$e_type = 'error';
			$n_params = array();
			$js = array( 'set_function_click_event', 'prepare_new_record_form_new' );
		
			$handle = isset($this->class_settings['html_replacement_selector']) && $this->class_settings['html_replacement_selector'] ? $this->class_settings['html_replacement_selector'] : 'inventories-asset-center-sub';

			$bypass = isset($o['bypass']) && $o['bypass'] ? $o['bypass'] : 0;
		
			$action_to_perform = $this->class_settings['action_to_perform'];
		
			switch( $action_to_perform ){
				
				case 'display_autogenerated_report':

					$id = $rty = '';

					$e = isset($o['record']) && $o['record'] ? $o['record'] : [];

					if( isset($_GET['rr']) && $_GET['rr'] && (isset( $_GET['br']) && $_GET['br']) && !$bypass  ){
						
						$id = rawurldecode( base64_decode( trim( $_GET['rr']) ) );

						$rty = rawurldecode( base64_decode( trim( $_GET['br']  ) ) );

						$pattern = '/^[a-zA-Z0-9_\-]+$/';
						if ( !preg_match( $pattern, $id) ) {
						   $error_msg = "<h4><b>Invalid Report</b></h4><p>Report Settings Unavailable</p>";
						}
						
						if( !$error_msg ){
							if ( !preg_match($pattern, $rty ) ) {
							   $error_msg = "<h4><b>Invalid Report</b></h4><p>Report Settings Unavailable</p>";
							}							
						}
					}

					if( !$error_msg ){
						if( $bypass ){

							$id = isset($o['id']) && $o['id'] ? $o['id'] : $id;

							$rty = isset($o['rty']) && $o['rty'] ? $o['rty'] : $rty;
						}

						if( $bypass || $id ){

							if( !$e ){

								$this->class_settings['current_record_id'] = $id;
								
								$e = $this->_get_record();
							}

							if( isset($e['id']) && $e['id'] ){

								if( ($e['login'] == 'yes') && !$bypass ){
									
									$filename = 'login-form.php';

									$this->class_settings['data'] = array( 'id' => $id, 'rty' => $rty, 'html_replacement' => $handle );
									
									$this->class_settings['html'] = array( $this->view_path . $filename );
									
									$html = $this->_get_html_view();
									
									return array(
										'status' => 'new-status',
										'html_replacement' => $html,
										'html_replacement_selector' => '#'.$handle,
										'javascript_functions' => $js
									);
								}

								$vat2 = array_flip( get_vat_mapping() );

								if( isset( $vat2[ $rty ] ) && in_array( $vat2[ $rty ], explode(":::", $e['report']) ) ){

									if( $bypass ){

										if( $e['user'] != $this->class_settings['user_id'] ){

											$error_msg = "<h4><b>Access Denied</b></h4><p>You do not have access to this report</p>";
										}
									}

									if( !$error_msg ){

										$tb = "reports_bay";
										
										$in = $this->plugin_instance->load_class( array( 'class' => [ $tb ], 'initialize' => 1 ) );
										
										$_GET = array(
											'default' => 'default',
											'report' => $rty,
											'no_side_bar' => 1,
											'spd' => 1
										);

										$in[ $tb ]->class_settings['action_to_perform'] = 'display_dashboard';

										$return = $in[ $tb ]->$tb();

										if( $bypass ){
											$_SESSION = array();
											error_reporting (~E_ALL);
											session_destroy();
										}

										return $return;
									}

								}else{
									$error_msg = "<h4><b>Access Denied</b></h4><p>You do not have access to this report</p>";
								}

							}else{
								$error_msg = "<h4><b>Invalid Report</b></h4><p>Report Settings Unavailable</p>";
							}
						}else{
							$error_msg = "<h4><b>Invalid Report</b></h4><p>Missing Report Parameter</p>";
						}
					}

				break;
				case 'save_login_report':
					$form_params = isset($_POST['formParams']) && $_POST['formParams'] ? json_decode($_POST['formParams'], true) : [];
					if( ( isset( $_POST["email"] ) || isset( $_POST["username"] ) ) && isset( $_POST["password"] ) ){
						$email = trim( ( isset( $_POST["email"] ) && $_POST["email"] )?$_POST["email"]:'' );
						$email_key = 'email';
						if( ! $email ){
							$email = ( isset( $_POST["username"] ) && $_POST["username"] )?$_POST["username"]:'';
							$email_key = 'username';
						}
						
						$pass = $_POST["password"];
					}else{
						$error_msg = "Please provide valid email & password";
					}
					
					if( !$error_msg ){
						
						$authentication = new cAuthentication();
						
						$authentication->class_settings = $this->class_settings;
						
						$authentication->class_settings["table"] = 'users';
						
						$authentication->class_settings["username_key"] = $email_key;
						
						$authentication->class_settings["username"] = $email;
						
						$authentication->class_settings["password"] = md5( $pass . get_websalter() );
						
						$authentication->class_settings["upassword"] = $pass;

						$authentication->class_settings["action_to_perform"] = 'confirm_username_and_password';
						
						$returned_data  = $authentication->authentication();
						
						if( is_array($returned_data) && isset( $returned_data[ 'typ' ] ) && $returned_data[ 'typ' ] == 'authenticated' && isset($returned_data[ 'user_details' ][ 'user_id' ]) && $returned_data[ 'user_details' ][ 'user_id' ] ){
							
							$this->class_settings['user_id'] = $returned_data[ 'user_details' ][ 'user_id' ];
							$this->class_settings['action_to_perform'] = 'display_autogenerated_report';
							
							return $this->_autogenerated_report_handler( array( 
								'bypass' => 1, 
								'id' => isset($form_params['id']) && $form_params['id'] ? $form_params['id'] : '',
								'rty' => isset($form_params['rty']) && $form_params['rty'] ? $form_params['rty'] : '',
							) );

						}else{
							$error_msg = "User could not be authenticated. Please provide valid email & password";
							if( isset( $returned_data["msg"] ) && $returned_data["msg"] ){
								$error_msg = $returned_data["msg"];
							}
						}
					}
				break;
			}
		
			$error_msg = $error_msg ? $error_msg : '<h4><b>Unknown Error</b></h4><p>Please try again later</p>';
		
			$n_params['type'] = $e_type;

			$n_params['message'] = $error_msg;

			$n_params['manual_close'] = 1;

			if( $bypass ){
				$_SESSION = array();
				error_reporting (~E_ALL);
